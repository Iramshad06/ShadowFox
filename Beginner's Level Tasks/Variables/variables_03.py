#3. Store the principal amount, rate of interest, and time indifferent variables and then calculate the Simple Interest for 3 years. Formula: Simple Interest = P x R x T / 100
Principal_amount=int(input("Enter the principal amount:"))
Rate_of_interest= int(input("Enter the rate_of_interest:")) 
Time_in_years=3
SimpleInterest_SI=(Principal_amount*Rate_of_interest*Time_in_years)/100
print(f"The Value of Simple Interest is = {SimpleInterest_SI}")