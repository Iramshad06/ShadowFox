#2. In a village, there is a circular pond with a radius of 84 meters. Calculate the area of the pond using the formula: Circle Area = πr^2. (Use the value 3.14 for π) 
# Bonus Question: If there is exactly 1.4 liters of water in a square meter, what is the total amount ofwater in the pond? Print the answer without any decimal point init.
r=84
pi =3.14
Area=pi*r*r
print("The Area of circle is:",Area)
#Pond has a Water amount of 1.4 litres in a square meter
Amount_of_water=1.4
Total_amount_of_water=(Area*Amount_of_water)
print("The total amount of water contained in the pool is:", Total_amount_of_water)